public class LableCount {
	private double label;
	private int count;
	public double getLabel() {
		return label;
	}
	public void setLabel(double label) {
		this.label = label;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public LableCount(double label, int count) {
		super();
		this.label = label;
		this.count = count;
	}
	
}
